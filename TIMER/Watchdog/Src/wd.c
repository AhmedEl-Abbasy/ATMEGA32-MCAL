void WDT_ON(u8 time_out)
{
	WDTCR_reg = 0;
	WDTCR_reg = (1<<WDE_pin)|time_out;
}
void WDT_OFF()
{
	//WDTCR_reg = (1<<WDTOE_pin)|(1<<WDE_pin);
	WDTCR_reg = 0b00011000;
	
	//WDTCR_reg &= ~(1<<WDE_pin);
	WDTCR_reg = 0b00010000;
}
